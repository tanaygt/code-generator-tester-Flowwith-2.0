def sort_list_ascending(numbers: list[int]) -> list[int]:
    """
    Sorts a list of integers in ascending order.

    This function takes a list of integers and returns a new list
    containing the elements sorted from smallest to largest. It does not
    modify the original list.

    Args:
        numbers: A list of integers to be sorted.

    Returns:
        A new list containing the integers sorted in ascending order.
    """
    return sorted(numbers)